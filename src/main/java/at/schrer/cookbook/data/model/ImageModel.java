package at.schrer.cookbook.data.model;

import javax.validation.constraints.NotBlank;

public class ImageModel {

    @NotBlank
    private String id;
    private String url;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
